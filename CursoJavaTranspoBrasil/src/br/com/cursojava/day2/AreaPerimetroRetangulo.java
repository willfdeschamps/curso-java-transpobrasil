package br.com.cursojava.day2;

public class AreaPerimetroRetangulo {

	public static void main(String[] args) {
		byte base = 20;
		byte altura = 30;
		
		System.out.printf("Área do triângulo: %d", (base * altura));
		System.out.printf("Perímetro do triângulo: %d", (base * 2) + (altura * 2));
	}
}
